def get_multiplier(symbol_code):
    """
    根据合约代码获取乘数
    :param symbol_code: 如 'SH0', 'M0', 'IF0'
    :return: 乘数 (int)
    """
    # 提取品种代码 (去除数字部分)
    # 大部分是 1-2 个字母 + 0 (主力连续)
    # 例如 SH0 -> SH, IF0 -> IF
    import re
    match = re.match(r"([A-Za-z]+)", symbol_code)
    if match:
        variety = match.group(1).upper()
        return FUTURES_MULTIPLIERS.get(variety, 10) # 默认 10
    return 10
